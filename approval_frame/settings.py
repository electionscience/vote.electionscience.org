# Keep your SECRET_KEY secret, and out of GitHub, by generating one for you
# if you don't have one already, in a file that we .gitignore
def generate_secret_key(filename):
    from django.utils.crypto import get_random_string
    chars = 'abcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*(-_=+)'
    key = get_random_string(50, chars)
    key_file = open(filename, 'w')
    key_file.write("SECRET_KEY = '"+key+"'")
    key_file.close()

